import pygame
from src.camera import camera

class TipoTile:
    def __init__(self, name, image, colisao):
        self.name = name
        self.image = pygame.image.load(image)
        self.colisao = colisao

class Map:
    def __init__(self, MAPA, tipos_tile, tamanho_tile):
        self.tipos_tile = tipos_tile
        self.tiles = MAPA
        self.tamanho_tile = tamanho_tile

    def desenhar_mapa(self, display):
        for y, linha in enumerate(self.tiles): #Passa um tupla de valores que serão usados, (index, linha)
            for x, tile in enumerate(linha):
                local = (x * self.tamanho_tile - camera.x , y * self.tamanho_tile - camera.y )
                image = self.tipos_tile[tile].image
                display.blit(image , local)

    def tem_colisao(self, rect_jogador):
        for y, linha in enumerate(self.tiles):
            for x, tile in enumerate(linha):

                # Verifica se aquele tile é uma borda
                if self.tipos_tile[tile].colisao:
                    rect_tile = pygame.Rect(
                        x * self.tamanho_tile,
                        y * self.tamanho_tile,
                        self.tamanho_tile,
                        self.tamanho_tile
                    )

                    if rect_jogador.colliderect(rect_tile):
                        return True

        return False


